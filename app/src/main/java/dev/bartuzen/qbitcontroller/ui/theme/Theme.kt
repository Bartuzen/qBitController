@Composable
fun AppTheme(darkTheme: Boolean = isSystemInDarkTheme(), dynamicColor: Boolean = true, content: @Composable () -> Unit) {
    val colorScheme = when {
        dynamicColor && Build.VERSION.SDK_INT >= Build.VERSION_CODES.S -> {
            val context = LocalContext.current
            if (darkTheme) dynamicDarkColorScheme(context) else dynamicLightColorScheme(context)
        }

        darkTheme -> darkScheme
        else -> lightScheme
    }

    val customColorsPalette = if (darkTheme) darkCustomColors else lightCustomColors

    MaterialTheme(colorScheme = colorScheme) {
        CompositionLocalProvider(
            LocalTextStyle provides MaterialTheme.typography.bodyMedium,
            LocalContentColor provides MaterialTheme.colorScheme.onBackground,
            LocalCustomColors provides customColorsPalette,
            LocalPreferenceTheme provides preferenceTheme(
                dialogOkText = stringResource(R.string.dialog_ok),
                dialogCancelText = stringResource(R.string.dialog_cancel),
            ),
            content = content,
        )
    }
}
